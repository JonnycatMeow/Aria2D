import Foundation
import Cocoa

extension NSColor {
	public class var customBackgroundColor: NSColor {
		return NSColor(calibratedRed: 0.97, green: 0.95, blue: 0.94, alpha: 1)
	}
	public class var customHightlightColor: NSColor {
		return NSColor(calibratedRed: 0.80, green: 0.78, blue: 0.77, alpha: 1)
	}
	
}



